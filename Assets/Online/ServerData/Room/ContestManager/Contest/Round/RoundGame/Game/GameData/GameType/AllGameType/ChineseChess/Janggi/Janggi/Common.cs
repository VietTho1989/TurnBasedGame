		public struct Move
		{
			public Pos From;
			public Pos To;

			public Move(sbyte fromX, sbyte fromY, sbyte toX, sbyte toY)
			{
				From = new Pos(fromX, fromY);
				To = new Pos(toX, toY);
			}

			public Move(Pos from, Pos to)
			{
				From = from;
				To = to;
			}

			public Move(int index)
			{
				this = moveSet[index];
			}

			public int GetIndex()
			{
				return move2index[this];
			}

			public static Move Empty = new Move(Pos.Empty, Pos.Empty);

			public bool IsEmpty
			{
				get {
					return From.IsEmpty && To.IsEmpty;
				}
			}

			public override string ToString()
			{
				return string.Format ("({0}, {1}) -> ({2}, {3})", From.X, From.Y, To.X, To.Y);
			}

			public Move GetOpposite()
			{
				Move op = new Move
				{
					From = From.GetOpposite(),
					To = To.GetOpposite()
				};
				return op;
			}

			public Move GetFlip()
			{
				Move flip = new Move
				{
					From = From.GetFlip(),
					To = To.GetFlip()
				};
				return flip;
			}

			public static List<Move> GetOpposite(List<Move> moves)
			{
				List<Move> ops = new List<Move>();

				foreach (var move in moves)
				{
					ops.Add(move.GetOpposite());
				}

				return ops;
			}


